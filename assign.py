# Initial variable
x = 10

# Copying the value of x to y
y = x

print("Value of x:", x)  # Output: Value of x: 10
print("Value of y:", y)  # Output: Value of y: 10

# Changing the value of y
y = 20
print("After modifying y:")
print("Value of x:", x)  # Output: Value of x: 10
print("Value of y:", y)  # Output: Value of y: 20
